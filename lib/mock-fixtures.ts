export function demoCoverStoragePath(slug: string): string {
  return `${COVER_PATH_PREFIX}/${slug}.png`
}

export function demoCoverBucket(): string {
  return COVER_BUCKET
}

// 从池中随机抽 n 条不重复；可选按 category 过滤、排除已有 slugs
export function sampleTopics(
  n: number,
  opts?: { category?: TopicCategory; excludeSlugs?: string[] }
): MockTopic[] {
  const exclude = new Set(opts?.excludeSlugs ?? [])
  const pool = DEMO_TOPICS.filter(
    (t) => (!opts?.category || t.category === opts.category) && !exclude.has(t.slug)
  )
  const out: MockTopic[] = []
  for (let i = 0; i < n && pool.length > 0; i++) {
    const idx = Math.floor(Math.random() * pool.length)
    out.push(pool.splice(idx, 1)[0])
  }
  return out
}

export function sampleInspiration(): MockInspiration {
  return DEMO_INSPIRATIONS[Math.floor(Math.random() * DEMO_INSPIRATIONS.length)]
}

export function isDemoMode(): boolean {
  // 默认 ON：未设置或非 'false' 都视为 demo 模式
  return process.env.DEMO_MODE !== 'false'
}
